from typing import List

class Solution:
    def search(self, nums: List[int], target: int) -> int:
        numslen = len(nums)
        if numslen == 0:
            return False
        left = 0
        right = numslen - 1

        while left < right:
            mid = (left + right) // 2
            if nums[mid] == target:
                return True
            # left to mid is sorted
            if nums[mid] > nums[left]:
                if target < nums[mid] and target >= nums[left]:
                    right = mid
                else:
                    left = mid + 1
            # mid to right is sorted
            elif nums[mid] < nums[left]:
                if target > nums[mid] and target < nums[left]:
                    left = mid + 1
                else:
                    left = mid
            # can't deciper the order
            else:
                left += 1
        if 0 <= left <= numslen -1 and nums[left] == target:
            return True
        if 0 <= right <= numslen -1 and nums[right] == target:
            return True
        return False

