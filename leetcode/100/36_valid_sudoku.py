class Solution:
    def isValidSudoku(self, board):
        data = [0] * 9
        def reset_data():
            for k in range(9):
                data[k] = 0
        def check_num(n):
            if n != ".":
                num = int(n) - 1
                if data[num] != 0:
                    return False
                else:
                    data[num] = 1
                    return True
            else:
                return True

        # Check rows
        for i in range(9):
            reset_data()
            for j in range(9):
                result = check_num(board[i][j])
                if not result:
                    return False
        # check columns
        for i in range(9):
            reset_data()
            for j in range(9):
                result = check_num(board[j][i])
                if not result:
                    return False
        # check 3x3
        for i in range(3):
            for j in range(3):
                reset_data()
                for k in range(3):
                    for l in range(3):
                        result = check_num(board[i*3+k][j*3+l])
                        if not result:
                            return False
        return True


